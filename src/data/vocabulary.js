// Get all words from all units
export const getAllWords = () => {
  return Object.values(vocabularyData).flatMap(unit => unit.words);
};

// Get words by unit
export const getWordsByUnit = (unitId) => {
  return vocabularyData[unitId]?.words || [];
};

// Get unit info
export const getUnitInfo = (unitId) => {
  const unit = vocabularyData[unitId];
  if (!unit) return null;
  return {
    id: unit.id,
    title: unit.title,
    icon: unit.icon,
    wordCount: unit.words.length
  };
};

// Get all units overview
export const getAllUnits = () => {
  return Object.values(vocabularyData).map(unit => ({
    id: unit.id,
    title: unit.title,
    icon: unit.icon,
    wordCount: unit.words.length
  }));
};

